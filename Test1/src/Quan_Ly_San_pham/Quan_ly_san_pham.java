    public void fillData()
    {   //Mothod show to Table
        DefaultTableModel tbModel = (DefaultTableModel)tbSanPham.getModel();
        tbModel.setRowCount(0); // clear table
        for(SanPham sp : daoSP.getAllSanPham())
        {
            Object rowData[] = new Object[7];
            rowData[0] = sp.getMaSP();
            rowData[1] = sp.getTenSP(); 
            rowData[2] = date_format.format(sp.getNgayNhap());                //sp.getNgayNhap();
            rowData[3] = sp.getLoaiSP();
            rowData[4] = sp.getSoLuong();
            rowData[5] = sp.getGia();
            rowData[6] = sp.getSoLuong()*sp.getGia();
            tbModel.addRow(rowData);
        }
    }
