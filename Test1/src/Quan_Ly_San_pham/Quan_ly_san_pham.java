    public boolean checkValidateForm()
    {
        if(txt_GaiBan.getText().isEmpty() || txt_MaSanPham.getText().isEmpty()
                || txt_NgayNhap.getText().isEmpty()
                || txt_SoLuong.getText().isEmpty()
                || txt_TenSanPham.getText().isEmpty())
        {
            return false;
        }
        return true;
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //button save
        if(!checkValidateForm())
        {
            JOptionPane.showMessageDialog(this, "Ban chua nhap day du thong tin");
        }
        else
        {
            try {
                SanPham sp = getModel();
                System.out.println("=>"+sp.toString());
                /*if(daoSP.add(sp)>0)
                {
                    LabelStatus.setText("Luu du lieu thanh cong");
                    LabelStatus.setForeground(Color.BLUE);
                    fillData();
                }*/
                if(btnSave.getText().equals("Luu"))
                {
                    if(daoSP.add(sp)>0)
                    {
                        LabelStatus.setText("Luu du lieu thanh cong");
                        LabelStatus.setForeground(Color.BLUE);
                        fillData();
                    }
                }
                else 
               {
                    if(daoSP.edit(sp)>0)
                    {
                        LabelStatus.setText("sua du lieu thanh cong");
                        LabelStatus.setForeground(Color.BLUE);
                        fillData();
                    }
                }
            } catch (ParseException ex) {
                System.out.println("Error"+ex.toString());
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        
        if(txt_MaSanPham.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma sp tim kiem");
        }else
        {
            SanPham sp = daoSP.findByID(txt_MaSanPham.getText().trim());
            if(sp !=null)
            {
                LabelStatus.setText("Da tim thay "+txt_MaSanPham.getText());
                LabelStatus.setForeground(Color.blue);
                //fill form
                setModel(sp);
            }else
            {
                LabelStatus.setText("ko tim thay"+txt_MaSanPham.getText());
                LabelStatus.setForeground(Color.red);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnFindActionPerformed

