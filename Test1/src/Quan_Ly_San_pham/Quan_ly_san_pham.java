    public SanPham getModel() throws ParseException
    {
        SanPham sp = new SanPham();
        sp.setMaSP(txt_MaSanPham.getText().trim());
        sp.setTenSP(txt_TenSanPham.getText().trim());
        sp.setGia(Double.parseDouble(txt_GaiBan.getText().trim()));
        sp.setSoLuong(Integer.parseInt(txt_SoLuong.getText().trim()));
        sp.setLoaiSP((LoaiSanPham)Cbo_LoaiSanPham.getSelectedItem());
        sp.setNgayNhap(date_format.parse(txt_NgayNhap.getText().trim()));
        return sp;
    }
    public void getDAtaCombobox()
    {
        DefaultComboBoxModel<LoaiSanPham> model = (DefaultComboBoxModel)Cbo_LoaiSanPham.getModel();
        
        for(LoaiSanPham loai: daoLoaiSP.getAllLoaiSP())
        {
            model.addElement(loai);
        }
    }
